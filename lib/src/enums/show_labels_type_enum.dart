/// This enum class is using to show the label text
enum ShowLabelsTypeEnum {
  /// Show only the top text label on single text
  showTopLabelType,

  /// Show only the bottom text label on single text
  showBottomLabelType,

  /// Show both top and bottom text label on single text
  showBothLabelsType,

  /// Disable both text label, this is the default value
  hideLabelsType,
}
